package Task1;

public class Swappingwiththird 
{
   public static void main(String[] args) {
	
	   int a=10;
	   int b=20;
	   int temp;
	   
	   System.out.println("Before swapping");
	   System.out.println("a=" +a +" "+ "b=" +b );
	   System.out.println("******************");
	   System.out.println("After swapping");
	   temp = a;
	   a=b;
	   b=temp;
	   System.out.println("a= "+a +" "+ "b="+b);
	   
}
}
